// Control Structures in JavaScript
// selection: if, if-else, if-else-if-else, switch
// if
let a = 10;
if (a > 5) {
  console.log("Greater than 5");
}
console.log("--------------------------");
// if-else
let b = 20;
if (a > b) {
  console.log("Greater than b");
} else {
  console.log("Less than or equal to b");
}
console.log("--------------------------");
// if-else-if-else
let score = 70;
if (score >= 80) {
  console.log("A");
} else if (score >= 70) {
  console.log("B");
} else if (score >= 60) {
  console.log("C");
} else if (score >= 50) {
  console.log("D");
} else {
  console.log("F");
}
console.log("--------------------------");
// switch
let busNo = 84;
switch (busNo) {
  case 84:
    console.log("Route Omyai - Krungthonburi");
    break;
  case 547:
    console.log("Route Salaya - Terminal 21 Rama 3");
    break;
  case 80:
    console.log("Route Watsinuanthammawimon - Sanam Luang");
    break;
  default:
    console.log("Route not found");
}
console.log("--------------------------");

