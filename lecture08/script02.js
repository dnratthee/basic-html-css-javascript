// Anonymous Function in JavaScript
// An anonymous function is a function that does not have a name. It is also known as a function expression. It is used to perform a task or calculate a value, and it is not accessible after its creation. It is used to pass as an argument to another function. It is also used to define a function inside an object.

let a = function () {
  console.log("Hello, World!");
};
a(); // Hello, World!

let b = function () {
  return "Hello, SAU!";
};
console.log(b()); // Hello, SAU!
